let arr = [1, 2, 3];
console.log(arr);

//Add to END
arr.push(4);
console.log(arr);

//Remove from END
arr.pop();
console.log(arr);

arr.push(5, 6);
console.log(arr);

//Add to begining
arr.unshift(0);
console.log(arr);

//Remove from begining
arr.shift(0);
console.log(arr);

arr.unshift(100);
console.log(arr);

arr.shift();
console.log(arr);

